receiving information that a recordable injury or illness has occurred.</li>
<li class="Dsc-li li-decor lab-head-u-normal">OSHA 301 Form, Injury and Illness Incident Report. Starting January 1,2001, injuries and 
illnesses at the company are recorded on this form within seven calendar days of receiving 
information that a recordable injury or illness has occurred.</li>
<li class="Dsc-li li-decor lab-head-u-normal">OSHA 300-A Form, Summary of Work-Related Injuries and Illnesses. This form is completed 
by the end of the year, posted no later than February 1 of the year following the year covered by 
the form (the first one must be posted in the year 2003), and kept posted in place until April 30.
</li>
</ul>
<h3 class="box-title">Annual Summary Posting</h3> 
<p class="lab-head-u-normal">At the end of each calendar year, Safety coordinator performs the following steps:</p>
<ol>
<li class="Dsc-li li-decor lab-head-u-normal">  Accident Report Forms. </li>
<li class="Dsc-li li-decor lab-head-u-normal">  Accident Investigation Forms. </li>


<li class="Dsc-li li-decor lab-head-u-normal">Reviews the OSHA 300 Form to verify that the entries are complete and accurate</li>
<li class="Dsc-li li-decor lab-head-u-normal">Corrects any deficiencies identified in the entries </li>
<li class="Dsc-li li-decor lab-head-u-normal">Creates OSHA 300-A, an annual summary of injuries and illnesses recorded on the OSHA 300 
Form</li>
<li class="Dsc-li li-decor lab-head-u-normal">Certifies that he/she or designee reasonably believes, based on his/her knowledge of the 
process by which the information was recorded, that the annual summary is correct and 
complete, and </li>
<li class="Dsc-li li-decor lab-head-u-normal">Posts OSHA 300-A from February 1 through April 30 of the year following the year covered 
by the form. 
</li>
<li class="Dsc-li li-decor lab-head-u-normal">Note: The 2001 summary (summary portion of OSHA 200 Log) must only be posted from 
February 1 to March 1,2002.
</li>
</ol>
<h3 class="box-title">Employee Involvement and Training</h3>	
<p class="lab-head-u-normal">This plan is an internal document guiding the action and behaviors of employees, so they need to 
know about it. At new hire orientation Safety coordinator or designee 
thoroughly explains to all employees why the Accident Reporting and Investigation Plan was 
prepared and how employees may be affected by it. Employees are informed of how to report an 
injury or illness.
</p>
<p class="lab-head-u-normal">Employees, and their representatives, are also provided limited access to our injury and illness 
records. Copies of relevant Log of Work-Related Injuries and Illnesses are provided by the next 
4 
Accident Reporting & Investigation Plan 
business day to all employees, former employees, and representatives that request them. 
Employees, former employees, and personal representatives who request Injury and Illness 
Incident Report will also receive them by the end of the next business day. However, authorized 
employee representatives will only receive requested Injury and Illness Incident Report within 
seven calendar days, and all sections of the Injury and Illness Incident Report will be removed 
except the 'Tell us about the case' section. All initial copies are provided to requestors free of 
charge. Additional copies involve a reasonable charge.
</p>
<p class="lab-head-u-normal">Our company does not discriminate against employees for:</p>
<ol>
 	<li class="Dsc-li li-decor lab-head-u-normal">Reporting a work-related fatality, injury, or illness;</li>
	<li class="Dsc-li li-decor lab-head-u-normal">Filing a safety and health complaint; </li>
	<li class="Dsc-li li-decor lab-head-u-normal">Asking for access to occupational injury and illness records; or </li>
	<li class="Dsc-li li-decor lab-head-u-normal">Exercising any rights afforded by the Occupational Safety and Health Act. </li>
</ol>
<h3 class="box-title">Program Evaluation </h3>
<p class="lab-head-u-normal">The Accident Reporting and Investigation Plan is evaluated and updated by Safety 
Director or designee annually to determine whether the plan is being followed and if further 
training may be necessary. 
</p>
