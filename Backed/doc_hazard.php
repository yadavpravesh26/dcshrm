working days after receiving information that a recordable injury or illness has occurred. This 
form is posted no later than February 1 following the year covered by the form, and shall remain in place until March 1. </li>
<li class="Dsc-li li-decor lab-head-u-normal">OSHA 300 Form, Log of Work-Related Injuries and Illnesses. Starting January 1,2001, 
