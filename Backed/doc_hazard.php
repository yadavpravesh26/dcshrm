                            <p class="lab-head-u-normal fire_u">The purpose of this program is to inform interested persons, including employees, that our company is complying with the OSHA Hazard Communication Standard, Title 29 Code of Federal Regulations 1910.1200. This is done by compiling a hazardous chemicals list, by using safety data sheets (SDSs), by ensuring that containers are labeled, and by providing our employees with training and information availability. 
</p>
<!--<h3 class="box-title">Administrative Duties </h3>-->
<p class="lab-head-u-normal">This program applies to all work operations in our company where employees may be exposed to 
hazardous substances under normal working conditions or during an emergency situation.  
</p>
<p class="lab-head-u-normal">The safety and health manager, or safety coordinator, is responsible for the program.</p>
<p class="lab-head-u-normal">All employees, or their designated representatives, can obtain further information on this written 
program, the hazard communication standard, applicable SDSs, and chemical information lists 
from the safety coordinator. Under this program, our employees will be informed of the contents of the Hazard Communication Standard, the hazardous properties of chemicals with which they work, safe handling procedures, and measures to take to protect themselves from these chemicals. Our employees will also be informed of the hazards associated with non-routine tasks.
</p>
<p class="lab-head-u-normal">If after reading this program, you find that improvements can be made, please contact the safety 
coordinator. We encourage all suggestions because we are committed to the success of our written hazard communication program. We strive for clear understanding, safe behavior, and involvement in the program from every level of the company. 
</p>
<h3 class="box-title">Hazard Evaluation Procedures </h3>
<p class="lab-head-u-normal">Our chemical inventory is a list of hazardous chemicals known to be present in our workplace. 
Anyone who comes into contact with the hazardous chemicals on the list needs to know what 
those chemicals are and how to protect themselves. That is why it is so important that hazardous 
chemicals are identified, whether they are found in a container or generated in work operations 
(for example, welding fumes, dusts, and exhaust fumes). The hazardous chemicals on the list can 
cover a variety of physical forms including liquids, solids, gases, vapors, fumes, and mists. 
Sometimes hazardous chemicals can be identified using purchase orders. Identification of others 
requires an actual inventory of the facility. The receiving clerk will notify the Safety Coordinator when new products arrive. The Safety Coordinator will add these items to the chemical inventory, which will be reviewed annually.
</p>
<p class="lab-head-u-normal">Safety Coordinator or designee updates the inventory as necessary. </p>
<p class="lab-head-u-normal">The safety coordinator keeps the chemical inventory list, along with related work practices used in our facility.</p>
<p class="lab-head-u-normal">The company does not manufacture any chemicals and, therefore, does not make any hazard 
determinations. We depend upon our manufactures to do the hazard determination for each 
product we use and or distribute. </p>
<p class="lab-head-u-normal">After the chemical inventory is compiled, it serves as a list of every chemical for which a SDS 
must be maintained. </p>
<p class="lab-head-u-normal">Safety Data Sheets (SDS)</p>
<p class="lab-head-u-normal">The SDS we use are fact sheets for chemicals, which pose a physical or health hazards in the 
workplace. SDS provides our employees with specific information on the chemicals they use. </p>
<p class="lab-head-u-normal">The safety coordinator or designee is responsible for obtaining/maintaining the SDS at our facility. They will contact the chemical manufacturer or vendor if additional research is necessary. All new procurements for the company must be cleared by the safety coordinator. </p>
<p class="lab-head-u-normal">The safety data sheets are kept at the following location(s) in our facility: </p>
<p class="lab-head-u-normal">The procedures followed if the SDS is not received at time of first shipment is: The Manager or 
designee will contact the distributor or manufacturer of the product for which the SDS is needed.
</p>
<p class="lab-head-u-normal">We do not generate SDS.</p>
<p class="lab-head-u-normal">Alternatives to SDS used in the workplace include: SDS information can be accessed by the 
intranet/internet. </p>
<p class="lab-head-u-normal">Labels and Other Forms of Warning </p>
<p class="lab-head-u-normal">Labels list at least the chemical identity, appropriate hazard warnings, and the name and address 
of the manufacturer, importer or other responsible party. The chemical identity is found on the 
label, the SDS, and the chemical inventory. Therefore, the chemical identity links these three 
sources of information. The chemical identity used by the supplier may be a common or trade 
name, or a chemical name. The hazard warning is a brief statement of the hazardous effects of 
the chemical (i.e., "flammable," or "causes lung damage"). Labels frequently contain other 
information, such as precautionary measures (i.e., "do not use near open flame"), but this 
information is provided voluntarily by our company and is not required by the rule. Our labels 
are legible and prominently displayed; though their sizes and colors can vary.</p>
<p class="lab-head-u-normal">The safety coordinator or designee is responsible for ensuring that all hazardous 
chemicals in containers are properly labeled and updated, as necessary. The safety coordinator or designee also ensures that newly purchased materials are checked for labels prior to use. </p>
<p class="lab-head-u-normal">A poster is displayed to inform employees about the hazard communication standard. It is 
located with the official postings of the company. 
</p>
<p class="lab-head-u-normal">If employees transfer chemicals from a labeled container to a portable container that is intended 
only for their IMMEDIATE use, no labels are required on the portable container.
</p>
<p class="lab-head-u-normal">No alternatives to labeling are used in this workplace.</p>
<p class="lab-head-u-normal">The following procedures are used to review and update label information when necessary and to 
ensure that labels that fall off or become unreadable are immediately replaced: Anyone finding 
product that has missing or unreadable labels will take the product to the HAZMA T area. They 
will report this to the Manager or designee who will take the appropriate action to get the label 
replaced. 
</p>
<h3 class="box-title">Training</h3>
<p class="lab-head-u-normal">Everyone who works with or is potentially "exposed" to hazardous chemicals will receive initial 
training and any necessary retraining on the Hazard Communication Standard and the safe use of 
those hazardous chemicals the safety coordinator or designee. "Exposure" means that "an employee is subjected to a hazardous chemical in the course of employment through any route of entry (inhalation, ingestion, skin contact or absorption, etc.) and includes potential (e.g., accidental or possible) exposure." Whenever a new hazard is introduced or an old hazard changes, additional training is provided.
</p>
<p class="lab-head-u-normal">Information and training is a critical part of the hazard communication program. We train our 
employees to read and understand the information on labels and SDS, determine how the 
information can be obtained and used in their own work areas, and understand the risks of 
exposure to the chemicals in their work areas as well as the ways to protect themselves. 
</p>
<p class="lab-head-u-normal">Our goal is to ensure employee comprehension and understanding including being aware that 
they are exposed to hazardous chemicals, knowing how to read and use labels and SDS, and appropriately following the protective measures we have established. We ask our employees to 
ask the safety coordinator or designee questions. As part of the assessment of the training program, Safety Person or designee asks for input from employees regarding the training they have received, and their suggestions for improving it. In this way, we hope to reduce any incidence of chemical source illnesses and injuries. 
</p>
<p class="lab-head-u-normal">All employees receive training for hazard communication. </p>








