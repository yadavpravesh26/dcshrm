<p>Accident Investigation Procedures</p>

<p>Thorough accident investigations will help the company determine why accidents occur, where<br />
they happen, and any trends that might be developing. Such identification is critical to preventing<br />
and controlling hazards and potential accidents. For all accident investigations, the supervisor, safety coordinator, a safety committee member or management designee will perform the following duties:</p>

<p>1. Conducts the accident investigation at the scene of the injury as soon after the injury as safely<br />
possible.</p>

<p>2. Asks the employee involved in the accident and any witnesses, in separate interviews, to tell<br />
him/her in their own words exactly what happened. He/ She does not interrupt or ask for more<br />
details at that time; he/she just lets the employee describe it in his/her own style.</p>

<p>3. Repeats the employee&#39;s version of the event back to the employee or witness and allows<br />
him/her to make any corrections or additions.</p>

<p>4. After the employee or witness has given his/her description of the event, asks appropriate<br />
questions that focus on causes.</p>

<p>5. Reminds the employee that the investigation is to determine the cause and possible corrective<br />
action that can eliminate the cause(s) of the accident.</p>

<p>6. Completes section &quot;Employee Data&quot; of the attached Accident Investigation Form with the<br />
employee, and review the data with the employee for accuracy. This will provide the information<br />
Safety coordinator or designee can put into database format. A copy of the Accident<br />
Investigation Form is to be sent to Safety coordinator or designee no more than one<br />
business day after the accident.</p>

<p>7. Uses the Accident Investigation Form for: Tracking and reporting injuries on a monthly basis;<br />
Grouping injuries by type, cause, body part affected, time of day, and process involved;<br />
Determining if any trends in injury occurrence exist and graphing those trends if possible;<br />
Identifying any equipment, materials, or environmental factors that seem to be commonly<br />
involved in injury incidents; Discussing with the safety committee and superiors the possible<br />
solutions to the problems identified; and Proceeding with improvements to reduce the likelihood<br />
of future injuries.</p>

<p>Injury, Illness, and Medical Issues</p>

<p>We also follow these procedures:</p>

<ol>
	<li>If a workplace accident results in an injury or illness requiring hospitalization of three or<br />
	more employees or a fatality of one or more employees, Safety Director reports the incident within eight hours by phone or in person to the nearest OSHA office.</li>
	<li>If an injured person is taken to a doctor, Safety coordinator will send the Accident Report form to Safety coordinator who will attach the doctor&#39;s statement to the Accident Report Form.</li>
	<li>If the injury or illness is &quot;recordable&quot; according to OSHA regulation, 29 CFR 1904, then<br />
	Safety coordinator or designee enters each recordable injury or illness on<br />
	OSHA Form 300, OSHA Form 301, and a separate, confidential list of privacy-concern<br />
	cases, if any, within seven calendar days of receiving information that a recordable injury<br />
	or illness has occurred.</li>
	<li>Employees with workplace injuries resulting in time off work shall be put in the<br />
	company&#39;s Return-to-Work Program to facilitate their full recovery and resumption of<br />
