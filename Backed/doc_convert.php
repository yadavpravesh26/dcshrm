<p><strong>Accident Reporting Procedures </strong></p>

<p>Our accident reporting procedures include the following:</p>

<ol>
	<li>Employees injured on the job are to report the injury to their supervisor (if possible) as<br />
	soon as possible after an incident/accident. &quot;Near miss&quot; accidents or incident should be<br />
